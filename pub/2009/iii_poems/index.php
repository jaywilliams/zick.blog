<h1>III Poems</h1>

<pre>
Together forever
For worse &amp; for better
No matter the weather
No matter whatever
Me &amp; my best friend
Together till the end
The friendship that hasn’t yet begun

I can’t believe I started it all
All by myself
But without displaying it
Up on the wall
I stuck it in a secret shelf
For me &amp; myself
I didn’t want no one to know
And by doing so
I destroyed the thing that I loved so

She runs across the beach
She with short blond hair
The wind blowing
Her smile showing
No one was ever so fair
</pre>

