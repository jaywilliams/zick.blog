<h1>Computational Poetry</h1>

<pre>
I lay awake at night
Aligning the cross-hairs
Clicking pixels away
Trying to remove my problems
      through use of positive &amp; negative space
But the subpixel render ruins me
Cause problems are never so cut &amp; dry
And when the X system fails
I'm left with an empty shell
</pre>

