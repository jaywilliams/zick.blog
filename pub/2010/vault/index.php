<h1>Vault</h1>

<pre>
What am I talking about
Do I have to shout
Yet I always sit and pout about
The way the love in my life has dried up into a drought
And I’ll always think I’m the dumbest one 
For getting into that mess at all
And that’s the way the story goes
But I haven’t always told it that way
It’s always someone else’s fault
Until they open up the vault
That is my brain
</pre>

