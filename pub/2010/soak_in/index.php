<h1>Soak In</h1>

<pre>
Water pouring down, pouring from the clouds
They’ve sprung a leak and now it’s spilling out
It can sometimes make me sad
It can sometimes make me glad
She loved the rain
So pour down rain
Rinse me out
Soak into my skin so I can shout:

            Hallelujah, the sky pours out his glory
            He reigns
</pre>

