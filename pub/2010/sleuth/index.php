<h1>Sleuth</h1>

<pre>
The snow falls on a day so cold
Here I stand in the midst
I so wish I had a hand to hold
So I’ll just give you the gist
If I may be so bold
There’s no girl that fits my list
This whole thing’s getting old

So to tell you the truth
I’d rather leave the truth untold
But I know you’re a sleuth
So I’ll tell you what’s been told
It’s so hard to find a Ruth
One whose heart will glow like gold

I sometimes feel it’s a fable
Sitting at the kitchen table
So for now I’ll label
Myself, for this, unable

Oh please make me ready
To take care of her
So I can hold her steady
When she is unsure
&amp; protect her from the deadly
Keeping her love pure
Singing her a medley
For warmth in the winter        <em>brrr</em>

I know she wont be ordinary
Just by reading the dictionary
But it will freeze my heart       <em>so scary</em>
Yet it seems more than arbitrary
That one day I should marry

The snow falls on a day so cold
If I may be so bold
This whole thing’s getting old
I so wish I had a hand
                                     <em>to hold</em>
</pre>

