<h1>Give Me A Spirit</h1>

<pre>
Keep my heart still
Though I can’t see beyond the next hill
Don’t let me dream the dreams in my head
Especially when I lay down to bed

But show me what to focus on instead
’cause sometimes I lay awake and bury my head
And though these arms and legs may become dust
My mind will exist somewhere else I trust

Don’t bear these thoughts upon me
But keep my eyes on the steps you let me take
Light with your lamp the path you gave me
Guide me in every decision I make
With your resolve I know you’ll prepare me
Give me a spirit that no one can shake
With your wondrous words you prevent me
From making a disastrous mistake
</pre>

