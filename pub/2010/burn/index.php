<h1>Burn</h1>

<pre>
Jumping fence posts hurts the most
When there’s toast on the other side
            Toast on the other side of the fence
Jumping fence posts hurts the worst
When you’ve got a thirst
            And you can’t drink toast

Toast ain’t that bad, though it makes you so sad
Sometimes even glad
Though it made her sad, mad

            But I’d rather eat this toast
            Than burn on the other side
</pre>

