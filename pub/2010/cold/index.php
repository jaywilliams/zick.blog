<h1>Cold</h1>

<pre>
The sky is clearer where the air is cooler
But not where it’s cold
Cause when it’s cold the conversation’s only old
Even though I love them so
Hot air is never the way to go

I’d rather be in the coldest snow
Than make the temperature so hot that they all go home

So now you’ll know
that I know
I love them so —
Even though the air I blow 
Can be as hot as you’ll never know
            I wish that weren’t so

So when I go to where the air is colder
At least the fires don’t even smolder
&amp; maybe one day I’ll grow older
            I hope so
</pre>

