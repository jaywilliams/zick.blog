<h1>I Forget</h1>

<pre>
I forget to learn from my mistakes
Until I have to hit the brakes
In the middle of my big race
So many problems I then face
Who will collide with me first?

Even I close my eyes
A plan will I devise
One using zero spies
They’re too expensive anyway
There I’ll stand in the month of May
In the midst of huge decay
What’s a plan for anyway?

I’m a perfectionist
The problems always persist
I always make big lists
But it might not be pure bliss
Cause it’s always a bit amiss
</pre>

