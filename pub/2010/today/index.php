<h1>Today</h1>

<pre>
Woke up
Itched my beard
This routine’s getting weird
I’ll get some water
Wash the dishes
I wish I could catch some fishes

The ink in this pen’s really low
I use it way too much you know
The rest of the family’s still asleep
Outside we’ve go some snow
I’ve got some promises I can still keep

I wish I had more time
But it’s God’s clock, not mine
I love the way the sun glitters off the snow

Ok now I’ll go
</pre>

