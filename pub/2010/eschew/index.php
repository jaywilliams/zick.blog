<h1>Eschew</h1>

<pre>
If only I knew
The times I had with you were few
There would be some things I’d not do
Cause I know your world I blew
Which was not a good thing for you
And that’s the thing I wish I had eschewed
Cause all the problems I brewed
Set the rest of your life in a bad mood
</pre>

