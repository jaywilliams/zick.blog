<h1>Ocean</h1>

<pre>
The turning tides will never wait
For me to articulate
Those things I always say too late
So I hit my head against the grate
And the only thing to wait is fate

And even though I know I’m just too slow
Yet now I’ll show
      that my heart don’t glow like snow
So now you can know
      that I’m just an old crow
That will prolly never let you grow
Cause I always tend to mow

The changing slides will never stop
Even when I’m at the top
But there I’ll always drop
It’s time to stop
</pre>

