<h1>Poems Thrice</h1>

<pre>
It’s too loud
I can’t focus myself
I wish I could put me on a shelf
      and organize
all the things I’ve memorized
I wish you were here
my dear sweat dear
I wish I could know who you are
but you’re so distant, so very far
I can’t figure things out
I wish I could shout
It’s too loud

I love you though you’re so very 
      far away
I wish I could know you
      and you me
but maybe we’ll both know someday

Give her a hedge of protection
While I try to reach perfection
I’ll prepare for her a place on day
So don’t let anyone lead her astray
Please let me appropriate
Everything so she wont have to wait
</pre>

