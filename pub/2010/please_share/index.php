<h1>Please Share</h1>

<pre>
This stress is heavy
Thick in the air
You can hear the medley
Sweet songs of despair
If it keeps going it’s deadly
So we need to repair
The countenance
Of our family tear
So sew it together
Sew it with care
Don’t let blood spill anywhere
It’ll get better
So jump in the air
And the story, please share
</pre>

