<h1>Need</h1>

<pre>
I’ve come to a conclusion,
Through all our turns and contusions:
We have too much, though not enough —
            We don’t need all the stuff
But we need more in our hearts and minds,
’cause without <em>you</em> we’re as good as blind
So for our lives to shine,

            We need no more than we need
</pre>

