I always feel like there are things every person should
revisit every once in a while. A book, if good enough,
should be worth reading and rereading. Some music should be
good enough to listen to on repeat. I have my own list of
&#147;axioms&#148; I like to revisit every so often, for each person
it&#146;s probably different.

Still, I considers these &#147;must reads&#148; no matter who
you are.



